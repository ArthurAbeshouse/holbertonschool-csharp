using System;

namespace MyMath
{
    /// <summary>
    /// Math operations 
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// adds integers
        /// </summary>
        /// <param name="a">1st int</param>
        /// <param name="b">2nd int</param>
        /// <returns></returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
